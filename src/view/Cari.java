    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        koneksi.KoneksiDatabase();

        String namaPembeli = jTextField1.getText();
        DefaultTableModel tabel = new DefaultTableModel();

        tabel.addColumn("Nama Pembeli");
        tabel.addColumn("Nama Barang");
        tabel.addColumn("Jumlah Beli");
        tabel.addColumn("Total Bayar");
        tabel.addColumn("Tanggal");

        jTable1.setModel(tabel);
        try {
            String sql = "select * from tbl_transaksi where namaPembeli like '%"+namaPembeli+"%'";

            ResultSet res = koneksi.state.executeQuery(sql);
            while (res.next()) {
                tabel.addRow(new Object[]{
                    res.getString("namaPembeli"),
                    res.getString("namaBarang"),
                    res.getString("jumlahBeli"),
                    res.getString("totalBayar"),
                    res.getString("tanggal")
                });
                jTable1.setModel(tabel);

            }
        } catch (SQLException eer) {
            System.err.println("SQLException:" + eer.getMessage());
        }
    }//GEN-LAST:event_jTextField1KeyReleased

